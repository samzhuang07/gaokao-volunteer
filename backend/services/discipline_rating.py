RATING_ORDER = {"A+": 0, "A": 1, "A-": 2, "B+": 3, "B": 4, "B-": 5, "C+": 6, "C": 7, "C-": 8}


class DisciplineRating:
    def __init__(self):
        self._ratings: dict[tuple[str, str], str] = {}
        self._loaded = False

    def _load(self):
        if self._loaded:
            return
        if not os.path.exists(CSV_PATH):
            return
        with open(CSV_PATH, "r", encoding="utf-8-sig") as f:
            reader = csv.DictReader(f)
            for row in reader:
                discipline = row["一级学科名称"].strip()
                rating = row["评估结果"].strip()
                for i in range(1, 28):
                    uni = row.get(f"学校名称{i}", "").strip()
                    if uni:
                        mapped_uni = UNIV_NAME_MAP.get(uni, uni)
                        key = (mapped_uni, discipline)
                        # Keep the highest rating if multiple entries for same pair
                        if key not in self._ratings or RATING_ORDER.get(rating, 99) < RATING_ORDER.get(self._ratings[key], 99):
                            self._ratings[key] = rating
        self._loaded = True

    def get_rating(self, university_name: str, major_category: str) -> str | None:
        """Get the discipline evaluation rating for a university + major category."""
        self._load()
        # Direct lookup by university + major category name
        key = (university_name, major_category)
        if key in self._ratings:
            return self._ratings[key]
        # Try to match via discipline-to-category mapping
        for discipline, category in DISCIPLINE_TO_MAJOR_CATEGORY.items():
            if category == major_category:
                key2 = (university_name, discipline)
                if key2 in self._ratings:
                    return self._ratings[key2]
        return None


rating_service = DisciplineRating()
